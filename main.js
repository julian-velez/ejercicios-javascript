// Definimos la función "sumar" que toma dos argumentos, "a" y "b", y devuelve su suma.
function sumar(a, b) {
  return a + b;
}

// Ejemplo de uso de la función "sumar"
console.log("Suma de 3 y 5:", sumar(3, 5)); // Suma de 3 y 5: 8

// Definimos la función "multiplicar" que toma dos argumentos, "a" y "b", y devuelve su producto.
function multiplicar(a, b) {
  return a * b;
}

// Ejemplo de uso de la función "multiplicar"
console.log("Producto de 4 y 6:", multiplicar(4, 6)); // Producto de 4 y 6: 24

// Definimos la función "dividir" que toma dos argumentos, "a" y "b", y devuelve su cociente.
function dividir(a, b) {
  return a / b;
}

// Ejemplo de uso de la función "dividir"
console.log("Cociente de 10 entre 2:", dividir(10, 2)); // Cociente de 10 entre 2: 5

// Definimos la función "concatenar" que toma dos argumentos, "a" y "b", y devuelve una cadena que es la concatenación de ambos.
function concatenar(a, b) {
  return a + " " + b;
}

// Ejemplo de uso de la función "concatenar"
console.log("Concatenación de 'Hola' y 'Mundo':", concatenar("Hola", "Mundo")); // Concatenación de 'Hola' y 'Mundo': Hola Mundo

// Definimos la función "comparar" que toma dos argumentos, "a" y "b", y devuelve verdadero si son iguales y falso si no lo son.
function comparar(a, b) {
  return a === b;
}

// Ejemplo de uso de la función "comparar"
console.log("Comparación de 7 y 7:", comparar(7, 7)); // Comparación de 7 y 7: true
console.log("Comparación de 7 y 8:", comparar(7, 8)); // Comparación de 7 y 8: false
